CREATE TABLE CATEGORIES (
  id_cat int(11) NOT NULL,
  nom_cat varchar(20) NOT NULL,
  desc_cat char(60) DEFAULT NULL,
  PRIMARY KEY (id_cat)
);

CREATE TABLE TREBALLADORS (
    id_treb int(11) NOT NULL,
    nom_treb varchar(20) NOT NULL,
    cognom_treb varchar(20) NOT NULL,
    anyNaix_treb date,
    notes text(450),
    PRIMARY KEY (id_treb)
);

CREATE TABLE CLIENTS (
    id_cli int(11) NOT NULL,
    nom_cli varchar(40) NOT NULL,
    nom_contacte varchar(30) NOT NULL,
    adre_cli varchar(50),
    pobl_cli varchar(20),
    cp_cli varchar(10),
    pais_cli varchar(20),
    PRIMARY KEY (id_cli)
);

CREATE TABLE PROVEIDORS (
    id_prov int(15) NOT NULL,
    nom_prov varchar(40) NOT NULL,
    nom_contacte varchar(30) NOT NULL,
    adre_prov varchar(50),
    pobl_prov varchar(20),
    cp_prov varchar(10),
    pais_prov varchar(20),
    tel_prov varchar(20),
    PRIMARY KEY (id_prov)
);

CREATE TABLE TRANSPORTISTES (
    id_tran int(11) NOT NULL,
    nom_tran varchar(20) NOT NULL,
    tel_tran varchar(20) NOT NULL,
    PRIMARY KEY (id_tran)
);

CREATE TABLE PRODUCTES (
    id_prod int(11) NOT NULL,
    nom_prod varchar(40) NOT NULL,
    id_prov int(15) NOT NULL,
    id_cat int(11) NOT NULL,
    unitats_prod char(25),
    preu_prod int(11) NOT NULL,
    PRIMARY KEY (id_prod),
    FOREIGN KEY (id_prov) REFERENCES PROVEIDORS(id_prov),
    FOREIGN KEY (id_cat) REFERENCES CATEGORIES(id_cat)
);

CREATE TABLE COMANDES (
    id_coma int(11) NOT NULL,
    id_cli int(11) NOT NULL,
    id_treb int(11) NOT NULL,
    data_coma date,
    id_tran int(11) NOT NULL,
    PRIMARY KEY (id_coma),
    FOREIGN KEY (id_cli) REFERENCES CLIENTS(id_cli),
    FOREIGN KEY (id_treb) REFERENCES TREBALLADORS(id_treb),
    FOREIGN KEY (id_tran) REFERENCES TRANSPORTISTES(id_tran)
);


CREATE TABLE DETALLCOMANDES (
    id_detall int(11) NOT NULL,
    id_coma int(11) NOT NULL,
    id_prod int(11) NOT NULL,
    Quantitat int(4) NOT NULL,
    PRIMARY KEY (id_detall),
    FOREIGN KEY (id_coma) REFERENCES COMANDES(id_coma),
    FOREIGN KEY (id_prod) REFERENCES PRODUCTES(id_prod)
);

